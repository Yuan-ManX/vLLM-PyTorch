from dataclasses import dataclass


@dataclass
class SamplingParams:
    """
    采样参数配置类，用于控制文本生成过程中的关键参数
    
    该类定义了生成文本时需要的主要控制参数，通过数据类特性简化实例化过程
    典型应用于语言模型生成、序列预测等需要采样策略的场景
    """

    # 温度参数，控制生成结果的随机性
    # 数值范围通常建议0.0-2.0：
    # - 接近0时结果更确定（贪心搜索）
    # - =1.0时符合原始概率分布
    # - >1.0时增加低概率结果的采样概率
    temperature: float = 1.0 

    # 最大生成token数
    # 控制生成文本的最大长度，实际应用时需要根据任务需求调整
    # 例如：对话系统可能需要更长，摘要任务需要更短
    max_tokens: int = 64

    # 是否忽略结束符
    # 当设为True时，模型会继续生成直到达到max_tokens限制
    # 适用于需要强制生成固定长度内容的场景
    ignore_eos: bool = False
